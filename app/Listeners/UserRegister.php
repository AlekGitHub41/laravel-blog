class UserRegister
{
    /**
     * Create the event listener.
     */
    public function __construct()
    {
        //
    }

    /**
     * Handle the event.
     */
    public function handle(UserRegistered $event): void
    {
        $user = $event->user;
        $user->profile()->create([
            "title" => "Title undefined",
            "description" => "Description undefined",
            "image"=>"/default.jpg"
        ]);
    }
}
